/**
 * Regional discovery layer for the Red List dataset.
 *
 * IMPORTANT COMPLIANCE NOTES:
 *  - A region listing means the species' native range / regular occurrence
 *    falls in that region, as established in the authoritative sources already
 *    linked on each record. It is NOT a claim of legal protection.
 *  - Global IUCN Red List category is what we show. National/regional legal
 *    status (national Red Books, endangered-species laws) is separate and
 *    varies by country — see the methodology page.
 *  - We do not invent presence. Species appear under a region only where their
 *    native range is well documented. "oceans" is used for marine species whose
 *    range spans ocean basins rather than a single landmass.
 *  - Region assignments are kept in one map here so every claim is reviewable.
 */

