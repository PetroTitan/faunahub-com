export function getRegionMeta(region: RedListRegion): RedListRegionMeta {
  return RED_LIST_REGIONS[region];
}

/** Regions a given slug is listed under (for record display). */
export function regionsForSlug(slug: string): RedListRegion[] {
  return REGION_ORDER.filter((r) => REGION_SPECIES[r].includes(slug));
}
