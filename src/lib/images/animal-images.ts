/**
 * Animal slugs we intend to cover but have not yet sourced a verified image
 * for. Currently empty — every existing animal profile page has a verified
 * hero image. Add entries here when new animal pages are introduced.
 */
export const ANIMAL_IMAGE_TODOS: readonly string[] = [];

const HERO_BY_SLUG: Record<string, AnimalImage> = Object.fromEntries(
  ANIMAL_IMAGES.filter((img) => img.role === "hero").map((img) => [
    img.animalSlug,
    img,
  ]),
);

const GALLERY_BY_SLUG: Record<string, AnimalImage[]> = ANIMAL_IMAGES.reduce(
  (acc, img) => {
    if (img.role !== "gallery") return acc;
    (acc[img.animalSlug] ??= []).push(img);
    return acc;
  },
  {} as Record<string, AnimalImage[]>,
);

/** Returns the verified hero image for an animal slug, or null if none. */
export function getAnimalImage(slug: string): AnimalImage | null {
  return HERO_BY_SLUG[slug] ?? null;
}

/** Alias of {@link getAnimalImage} for clarity at call sites that explicitly
 * want the hero. */
export function getAnimalHeroImage(slug: string): AnimalImage | null {
  return HERO_BY_SLUG[slug] ?? null;
}

/** Returns the verified gallery images for an animal slug, in insertion order.
 * Returns an empty array if none. */
export function getAnimalGalleryImages(slug: string): AnimalImage[] {
  return GALLERY_BY_SLUG[slug] ?? [];
}

/** Returns hero + gallery images for an animal, hero first. */
export function getAnimalImages(slug: string): AnimalImage[] {
  const hero = HERO_BY_SLUG[slug];
  const gallery = GALLERY_BY_SLUG[slug] ?? [];
  return hero ? [hero, ...gallery] : gallery;
}

/** Returns every image record across every animal. */
export function getAllAnimalImages(): readonly AnimalImage[] {
  return ANIMAL_IMAGES;
}

/** Absolute URL helper for OG image tags. */
export function getAnimalImageAbsoluteUrl(slug: string): string | null {
  const img = getAnimalImage(slug);
  if (!img) return null;
  return `https://faunahub.com${img.localPath}`;
}

/** Builds an OG image input for `buildArticleMetadata`. Returns undefined if no
 * image is available, so callers can spread it conditionally. */
export function getAnimalOgImage(
  slug: string,
):
  | { url: string; width: number; height: number; alt: string }
  | undefined {
  const img = getAnimalImage(slug);
  if (!img) return undefined;
  return {
    url: `https://faunahub.com${img.localPath}`,
    width: img.width,
    height: img.height,
    alt: img.alt,
  };
}
